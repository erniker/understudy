#!/usr/bin/env python3
"""Dogfood harness for understudy-compress.

Runs the real `modules/caveman/bin/understudy-compress` against copies of
every Markdown file under `templates/docs/` and reports the token reduction
in a markdown table. Touches no source files: every input is copied to a
temp dir before compression.

Usage:
    python3 modules/caveman/evals/dogfood.py [--targets DIR ...] [--out PATH]

Defaults:
    --targets templates/docs
    --out      modules/caveman/evals/RESULTS.md
               (the dogfood table is rewritten between the markers
               <!-- DOGFOOD-START --> and <!-- DOGFOOD-END -->)
"""

from __future__ import annotations

import argparse
import shutil
import subprocess
import sys
import tempfile
from pathlib import Path

HERE = Path(__file__).resolve().parent
# HERE = <root>/modules/caveman/evals  ->  ROOT three levels up.
ROOT = HERE.parent.parent.parent
COMPRESS = ROOT / "modules" / "caveman" / "bin" / "understudy-compress"

sys.path.insert(0, str(HERE))
from measure import count_tokens, method  # noqa: E402


def collect_markdown(targets: list[Path]) -> list[Path]:
    out: list[Path] = []
    for t in targets:
        if t.is_file() and t.suffix == ".md":
            out.append(t)
        elif t.is_dir():
            out.extend(sorted(p for p in t.rglob("*.md") if p.is_file()))
    return out


def compress_copy(src: Path, workdir: Path) -> Path:
    dst = workdir / src.name
    shutil.copy2(src, dst)
    res = subprocess.run(
        [sys.executable, str(COMPRESS), "--no-install", dst.name],
        cwd=workdir,
        capture_output=True,
        text=True,
    )
    if res.returncode != 0:
        sys.stderr.write(f"compress failed for {src}: {res.stderr}\n")
        sys.exit(1)
    return dst


def fmt_pct(before: int, after: int) -> str:
    if before == 0:
        return "n/a"
    return f"{(before - after) * 100.0 / before:.1f}%"


def render_table(rows: list[dict], tok_method: str) -> str:
    lines = []
    lines.append(f"_Token method: `{tok_method}`. Generated by `modules/caveman/evals/dogfood.py`._")
    lines.append("")
    lines.append("| File | Before (tok) | After (tok) | Reduction | Bytes before | Bytes after |")
    lines.append("|---|---:|---:|---:|---:|---:|")
    tot_b = tot_a = bb = ba = 0
    for r in rows:
        lines.append(
            f"| `{r['rel']}` | {r['tok_before']} | {r['tok_after']} | "
            f"{fmt_pct(r['tok_before'], r['tok_after'])} | "
            f"{r['bytes_before']} | {r['bytes_after']} |"
        )
        tot_b += r["tok_before"]
        tot_a += r["tok_after"]
        bb += r["bytes_before"]
        ba += r["bytes_after"]
    lines.append(
        f"| **TOTAL** | **{tot_b}** | **{tot_a}** | **{fmt_pct(tot_b, tot_a)}** | "
        f"**{bb}** | **{ba}** |"
    )
    return "\n".join(lines)


def replace_block(content: str, marker_start: str, marker_end: str, new_inner: str) -> str:
    if marker_start not in content or marker_end not in content:
        return content + f"\n\n{marker_start}\n{new_inner}\n{marker_end}\n"
    head, _, rest = content.partition(marker_start)
    _, _, tail = rest.partition(marker_end)
    return f"{head}{marker_start}\n{new_inner}\n{marker_end}{tail}"


def main() -> int:
    p = argparse.ArgumentParser(description="Dogfood understudy-compress on Markdown trees.")
    p.add_argument("--targets", nargs="+", default=[str(ROOT / "templates" / "docs")])
    p.add_argument("--out", default=str(HERE / "RESULTS.md"))
    p.add_argument("--label", default="templates/docs/",
                   help="Heading label for this dogfood section.")
    p.add_argument("--marker-id", default="DOGFOOD",
                   help="Marker base name; section is rewritten between"
                        " <!-- {ID}-START --> and <!-- {ID}-END -->.")
    args = p.parse_args()

    targets = [Path(t).resolve() for t in args.targets]
    files = collect_markdown(targets)
    if not files:
        sys.stderr.write("no markdown files matched\n")
        return 2

    rows: list[dict] = []
    with tempfile.TemporaryDirectory(prefix="understudy-dogfood-") as tmp:
        wd = Path(tmp)
        for src in files:
            try:
                rel = src.relative_to(ROOT)
            except ValueError:
                rel = src
            before_text = src.read_text(encoding="utf-8", errors="replace")
            tok_before, _ = count_tokens(before_text)
            bytes_before = len(before_text.encode("utf-8"))

            dst = compress_copy(src, wd)
            after_text = dst.read_text(encoding="utf-8", errors="replace")
            tok_after, _ = count_tokens(after_text)
            bytes_after = len(after_text.encode("utf-8"))

            rows.append(
                {
                    "rel": str(rel).replace("\\", "/"),
                    "tok_before": tok_before,
                    "tok_after": tok_after,
                    "bytes_before": bytes_before,
                    "bytes_after": bytes_after,
                }
            )

    table = render_table(rows, method())
    out = Path(args.out)
    if out.exists():
        existing = out.read_text(encoding="utf-8")
    else:
        existing = (
            "# Caveman evals — results\n\n"
            "> Regenerate with `./modules/caveman/evals/run.sh`. Do not\n"
            "> edit by hand; sections are rewritten between marker comments.\n"
            "> See [`modules/caveman/evals/README.md`](README.md) for methodology.\n"
        )
    start = f"<!-- {args.marker_id}-START -->"
    end = f"<!-- {args.marker_id}-END -->"
    new = replace_block(existing, start, end, f"## Dogfood: {args.label}\n\n" + table)
    out.write_text(new, encoding="utf-8")
    print(table)
    print(f"\nWrote {out}")
    return 0


if __name__ == "__main__":
    sys.exit(main())

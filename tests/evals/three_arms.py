#!/usr/bin/env python3
"""Three-arm context-token harness for caveman mode.

Builds three context bundles for the SAME hypothetical user task and reports
token counts. The three arms are:

    A. no-caveman:      base context (AGENTS.md template + spec.md template).
    B. role:            same context + the caveman role prepended.
    C. role+compress:   same context, but every Markdown bundle file is run
                        through `scripts/understudy-compress` first, and the
                        caveman role is prepended.

This measures the **input-side** token cost — what the model reads before it
emits a single token. Output-side savings (the role's effect on what the model
writes) cannot be measured without an LLM call; that arm is documented in the
README and left to teams that wire this into their agent runner.

Usage:
    python3 tests/evals/three_arms.py [--out PATH]

Output:
    A markdown table written between <!-- ARMS-START --> / <!-- ARMS-END -->
    in tests/evals/RESULTS.md by default.
"""

from __future__ import annotations

import argparse
import shutil
import subprocess
import sys
import tempfile
from pathlib import Path

HERE = Path(__file__).resolve().parent
ROOT = HERE.parent.parent
COMPRESS = ROOT / "scripts" / "understudy-compress"

sys.path.insert(0, str(HERE))
from measure import count_tokens, method  # noqa: E402

# Files that make up the "context bundle" for the simulated task. We pick
# real Understudy artefacts so the numbers reflect actual deployments.
BUNDLE = [
    ROOT / "templates" / "AGENTS.md",
    ROOT / "templates" / "docs" / "spec.md",
    ROOT / "templates" / "docs" / "decisions.md",
]
ROLE = ROOT / "roles" / "caveman.instructions.md"
TASK = HERE / "fixtures" / "task.md"


def compress_text(text: str, name_hint: str) -> str:
    with tempfile.TemporaryDirectory(prefix="understudy-arms-") as tmp:
        wd = Path(tmp)
        f = wd / name_hint
        f.write_text(text, encoding="utf-8")
        res = subprocess.run(
            [sys.executable, str(COMPRESS), "--no-install", f.name],
            cwd=wd,
            capture_output=True,
            text=True,
        )
        if res.returncode != 0:
            sys.stderr.write(f"compress failed for {name_hint}: {res.stderr}\n")
            sys.exit(1)
        return f.read_text(encoding="utf-8")


def read(path: Path) -> str:
    return path.read_text(encoding="utf-8", errors="replace")


def fmt_pct(before: int, after: int) -> str:
    if before == 0:
        return "n/a"
    return f"{(before - after) * 100.0 / before:.1f}%"


def main() -> int:
    p = argparse.ArgumentParser(description="3-arm context-token measurement for caveman mode.")
    p.add_argument("--out", default=str(HERE / "RESULTS.md"))
    args = p.parse_args()

    for f in BUNDLE + [ROLE, TASK]:
        if not f.exists():
            sys.stderr.write(f"missing fixture: {f}\n")
            return 2

    task = read(TASK)
    role = read(ROLE)
    bundle_raw = [read(p) for p in BUNDLE]
    bundle_compressed = [compress_text(t, p.name) for t, p in zip(bundle_raw, BUNDLE)]
    role_compressed = compress_text(role, ROLE.name)

    arm_a = "\n\n".join(bundle_raw + [task])
    arm_b = "\n\n".join([role] + bundle_raw + [task])
    arm_c = "\n\n".join([role_compressed] + bundle_compressed + [task])

    a, _ = count_tokens(arm_a)
    b, _ = count_tokens(arm_b)
    c, _ = count_tokens(arm_c)

    lines = []
    lines.append(f"_Token method: `{method()}`. Generated by `tests/evals/three_arms.py`._")
    lines.append("")
    lines.append("Bundle:")
    for f in BUNDLE:
        lines.append(f"- `{f.relative_to(ROOT).as_posix()}`")
    lines.append(f"- Role: `{ROLE.relative_to(ROOT).as_posix()}` (arms B and C only)")
    lines.append(f"- Task: `{TASK.relative_to(ROOT).as_posix()}` (all arms)")
    lines.append("")
    lines.append("| Arm | What | Input tokens | vs. A |")
    lines.append("|---|---|---:|---:|")
    lines.append(f"| A | no-caveman (baseline)        | {a} | — |")
    lines.append(f"| B | role only (prepended)        | {b} | {fmt_pct(a, b)} |")
    lines.append(f"| C | role + compressed bundle     | {c} | {fmt_pct(a, c)} |")
    lines.append("")
    lines.append(
        "Arm B is **larger** than A by design — the role itself costs tokens. "
        "Its return on investment is on the **output** side (what the model "
        "writes), which this harness does not measure. Arm C compresses both "
        "the role and the bundle; whether C drops below A depends on how "
        "prose-heavy the bundle is. Terse bundles (tables, bullets) recover "
        "less of the role's cost than prose-heavy ones."
    )

    table = "\n".join(lines)
    out = Path(args.out)
    if out.exists():
        existing = out.read_text(encoding="utf-8")
    else:
        existing = (
            "# Caveman evals — results\n\n"
            "> Regenerate with `./tests/evals/run.sh`. See "
            "[`tests/evals/README.md`](README.md) for methodology.\n"
        )
    block = "## Three-arm context measurement\n\n" + table
    if "<!-- ARMS-START -->" in existing and "<!-- ARMS-END -->" in existing:
        head, _, rest = existing.partition("<!-- ARMS-START -->")
        _, _, tail = rest.partition("<!-- ARMS-END -->")
        new = f"{head}<!-- ARMS-START -->\n{block}\n<!-- ARMS-END -->{tail}"
    else:
        new = existing + f"\n\n<!-- ARMS-START -->\n{block}\n<!-- ARMS-END -->\n"
    out.write_text(new, encoding="utf-8")
    print(table)
    print(f"\nWrote {out}")
    return 0


if __name__ == "__main__":
    sys.exit(main())
